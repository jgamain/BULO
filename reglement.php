		<h2> Respect des documents et des biens</h2>

		<DL>
			<DT> Respect des documents </DT>
			<br>
			<DD>Il est demandé aux usagers de prendre soin des documents qui leur sont communiqués ou prêtés.<br>
			Les collections des bibliothèques étant un bien commun, il est strictement interdit d’écrire, de dessiner ou de faire une quelconque marque sur les documents, de plier ou de corner les pages.<br>
			Afin d’assurer la longévité des collections, les usagers doivent s’abstenir de toute intervention sur les documents. Les lecteurs qui remarqueraient des documents en mauvais état sont invités à les signaler aux bibliothécaires qui feront effectuer la réparation par un professionnel.
			</DD>
			<br>
			<DT> Contrôle antivol </DT>
			<br>
			<DD> Tous les documents sont équipés contre le vol. Les usagers sont responsables de tous les documents trouvés en leur possession : ils doivent s’assurer avant de franchir le contrôle antivol qu’ils n’ont sur eux aucun document dont le prêt n'a pas été préalablement enregistré. Si le système de détection se déclenche lors de leur passage, ils doivent se présenter à la banque de prêt et remettre leur carte de bibliothèque. Ils sont invités à se prêter aux contrôles des sacs, sacoches et porte-documents en leur possession par le personnel de la bibliothèque concernée. 
			</DD>
			<br>
			<DT> Pénalités en cas de retard </DT>
			<br>
			<DD> Tout retard dans la restitution des documents entraînera une suspension du prêt à domicile dans l’ensemble des bibliothèques du Service Commun de la Documentation égale au nombre de jours de retard.
			Des lettres de rappel sont envoyées aux lecteurs en retard sur leur adresse mail. Tout changement d’adresse doit être signalé à la bibliothèque.
			En cas de non-restitution ou perte d’un document, l’emprunteur devra le racheter ou le rembourser d’après un devis établi par la bibliothèque correspondant au prix d’achat TTC de la dernière édition ou au prix réactualisé s’il n’est plus édité.
			</DD>
			
